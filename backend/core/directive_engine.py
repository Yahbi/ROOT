def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


# ── Data models ──────────────────────────────────────────────────


@dataclass(frozen=True)
class Directive:
    """An autonomous strategic directive generated by ROOT."""
    id: str
    title: str
    rationale: str
    priority: int = 5  # 1=critical, 5=normal, 9=background
    category: str = "general"  # trading, learning, automation, research, health, product
    assigned_agents: tuple[str, ...] = ()
    collab_pattern: str = "delegate"  # delegate | pipeline | fanout | council
    task_description: str = ""
    status: str = "proposed"  # proposed | executing | completed | failed | expired
    result: Optional[str] = None
    source_signals: tuple[str, ...] = ()
    ttl_minutes: int = 120
    created_at: str = field(default_factory=_now_iso)
    executed_at: Optional[str] = None
    completed_at: Optional[str] = None
    # Impact scoring
    impact_score: float = 0.5          # 0.0-1.0 estimated value
    impact_label: str = "medium"       # low | medium | high | critical
    estimated_value_usd: float = 0.0   # dollar value estimate when applicable
    # Conflict tracking
    conflict_flags: tuple[str, ...] = ()   # IDs of conflicting active directives
    # Approval fields
    approval_reason: str = ""
    approval_benefit: str = ""
    approval_risk: str = ""
    # Template tracking
    template_id: Optional[str] = None     # source template if spawned from one


